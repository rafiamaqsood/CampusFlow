class AuthChecker extends StatelessWidget {
  const AuthChecker({super.key});

  @override
  Widget build(BuildContext context) {
    final authService = AuthService();
    return StreamBuilder<User?>(
      stream: authService.user,
      builder: (context, snapshot) {
        if (snapshot.connectionState == ConnectionState.waiting) {
          return const Scaffold(
            body: Center(child: CircularProgressIndicator()),
          );
        }
        
        if (snapshot.hasData) {
          return FutureBuilder<UserModel?>(
            future: _getProfileWithRetry(authService, snapshot.data!.uid),
            builder: (context, userSnapshot) {
              if (userSnapshot.connectionState == ConnectionState.waiting) {
                return const Scaffold(
                  body: Center(child: CircularProgressIndicator()),
                );
              }
              
              if (userSnapshot.hasData && userSnapshot.data != null) {
                final user = userSnapshot.data!;
                switch (user.role) {
                  case UserRole.student:
                    return StudentDashboard(user: user);
                  case UserRole.faculty:
                    return FacultyDashboard(user: user);
                  case UserRole.admin:
                    return AdminDashboard(user: user);
                  case UserRole.viceChancellor:
                    return VCDashboard(user: user);
                }
              }
              
              // If we still have no data, maybe the profile creation failed or is delayed
              return const LoginScreen();
            },
          );
        }
        
        return const LoginScreen();
      },
    );
  }

  // Helper to retry fetching profile in case of race condition during signup
  Future<UserModel?> _getProfileWithRetry(AuthService authService, String uid) async {
    UserModel? user = await authService.getUserData(uid);
    if (user == null) {
      // Wait 1 second and retry up to 3 times
      for (int i = 0; i < 3; i++) {
        await Future.delayed(const Duration(seconds: 1));
        user = await authService.getUserData(uid);
        if (user != null) break;
      }
    }
    return user;
  }
}

